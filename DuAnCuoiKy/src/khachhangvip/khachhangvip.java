package khachhangvip;

import java.util.Date;

public class khachhangvip {
public String mathe ;
String cmnd;
String name;
String phone;
String email;
Date ngaydangki;
String soly , chietkhau;




public khachhangvip(String mathe, String cmnd, String name, Date ngaydangki, String phone, String email, String soly,
		String chietkhau) {
	super();
	this.mathe = mathe;
	this.cmnd = cmnd;
	this.name = name;
	this.ngaydangki = ngaydangki;
	this.phone = phone;
	this.email = email;
	this.soly = soly;
	this.chietkhau = chietkhau;
}
public String getMathe() {
	return mathe;
}
public void setMathe(String mathe) {
	this.mathe = mathe;
}
public String getCmnd() {
	return cmnd;
}
public void setCmnd(String cmnd) {
	this.cmnd = cmnd;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getPhone() {
	return phone;
}
public void setPhone(String phone) {
	this.phone = phone;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public Date getNgaydangki() {
	return ngaydangki;
}
public void setNgaydangki(Date ngaydangki) {
	this.ngaydangki = ngaydangki;
}
public String getSoly() {
	return soly;
}
public void setSoly(String soly) {
	this.soly = soly;
}
public String getChietkhau() {
	return chietkhau;
}
public void setChietkhau(String chietkhau) {
	this.chietkhau = chietkhau;
}




}
	
	
	
	
	

